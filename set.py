#要创建一个set，需要提供一个list作为输入集合
#set可以看成数学意义上的无序和无重复元素的集合
s = set([1, 2, 3])

#输出{1, 2, 3}
s = set([1, 1, 2, 2, 3, 3])

#add(key)
s.add(4)
s.remove(4)

s1 = set([1, 2, 3])
s2 = set([2, 3, 4])

print('交集: ',s1 & s2) #取交集 {2, 3}
print('并集: ',s1|s2) #取并集 {1, 2, 3, 4}

s3 = set((1, 2, 3))
print('s3 = ', s3)

#tuple虽然是不变对象，但试试把(1, 2, 3)和(1, [2, 3])放入dict或set中，并解释结果
s4 = set((1, 2, 3))
print('s4 = ',s4)